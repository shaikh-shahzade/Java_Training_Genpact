package com.org.gen.day6;

public class Employee {

	String name;
	int id;
	int salary;
	int experience;
	String skillSet;
	public Employee(String name, int id, int salary, int experience, String skillSet) {
		super();
		this.name = name;
		this.id = id;
		this.salary = salary;
		this.experience = experience;
		this.skillSet = skillSet;
	}
}
